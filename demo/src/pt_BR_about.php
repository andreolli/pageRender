                        destinada ao fomento da pesquisa científica e tecnológica e à formação
                        de recursos humanos para a pesquisa no país.</p>
                    <p>A Corollarium Teccnologia foi aprovada no edital RHAE Pesquisador
                        na Empresa, com 18 meses de duração a partir de setembro de 2011.</p>
