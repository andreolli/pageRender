        <div><p>Em 2005, um grupo de pesquisadores saindo da pós-graduação, com experiência em desenvolvimento de software
                na área de computação gráfica e distribuída, começou a trabalhar em conjunto, desenvolvendo soluções de Realidade Virtual,
                Realidade Aumentada e Processamento de Imagens. Esta foi a semente da Corollarium, que desde então expandiu e cresceu.</p>

            <p>Por desenvolver toda a sua tecnologia internamente, a Corollarium
                pode criar soluções customizadas para suas necessidades, consulte-nos.</p>

            <h1>Editais e investimentos</h1>
            <p>A Corollarium foi contemplada com vários editais, investimentos e parcerias de
                importantes instituições. Além disso, temos experiência na escrita e submissão de projetos
                para editais, auxiliando nossos clientes na obtenção de subvenção e investimento para bancar
                seus projetos.	</p>

            <div>
                <div><br /><h2><a href="http://www.culturadigital.br/xpta/">
                            XPTA.LAB (Ministério da Cultura do Brasil)</a></h2>
                    <p>
                        Corollarium foi responsável por três projetos de código aberto no
                        <a href="http://www.culturadigital.br/xpta/">projeto XPTA.LAB</a>,
                        um programa do Ministério da Cultura do Brasil ocorrido em 2010. Os projetos
                        incluíram: um projeto de arte virtual baseado
                        na obra de <a href="http://www.mennabarreto.com.br">Sonia Menna Barreto</a>;
                        jogos colaborativos para TV Digital voltados para deficientes visuais; e um projeto
                        de interação corporal por reconhecimento de imagens,
                        <a href="http://skamleton.sf.net">skamleton</a>, que pode ser usado para jogos e
                        realidade virtual.</p>
                </div>


                <div><br /><h2><a href="http://www.fapesp.br/">FAPESP</a></h2>
                    <p>A FAPESP (Fundação para Pesquisa de São Paulo) é uma fundação pública e independente com
                        a missão de promover a pesquisa e o desenvolvimento científico e tecnológico do estado
                        de São Paulo. FAPESP outorgou à Corollarium Tecnologia um projeto PIPE em 2009, para
                        pesquisa inovadora em
                        sistemas de realidade virtual de baixo custo.</p></div><div><h2><a href="http://www.finep.gov.br">FINEP</a></h2>
                    <p>A FINEP (Financiadora de Pesquisa e Projetos), também conhecida como a Agência Brasileira de
                        Inovação, é uma instituição pública com o propósito de financiar pesquisa científica e tecnológica
                        e cursos de pós-graduação em universidades e instituições de pesquisa brasileiras, assim como
                        pesquisa e desenvolvimento em empresas.</p>

                    <p>A Corollarium Tecnologia foi aprovada no Projeto PRIME em Dezembro de 2009, com um projeto
